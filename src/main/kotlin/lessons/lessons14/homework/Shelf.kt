package lessons.lessons14.homework

class Shelf(var capacity: Int) {
    val items = mutableListOf<String>()
    val currentSum = items.sumOf { it.length }

    fun addItem(itemName: String): Boolean {
        if(!canAccommodate(itemName)) {
            items.add(itemName)
            return true
        }
        return false
    }

    fun removeItem(itemName: String): Boolean {
        if(items.contains(itemName)) {
            items.remove(itemName)
            return true
        }
        return false
    }

    fun canAccommodate(itemName: String): Boolean {
           return currentSum + itemName.length <= capacity
    }

    fun containsItem(itemName: String): Boolean {
        return items.contains(itemName)
    }

     fun getItems(): List<String> {
        return items.toList()
    }
    }

